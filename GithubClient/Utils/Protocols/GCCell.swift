import UIKit

protocol GCCell {
    static var identifier : String {get}
    static var nib:UINib {get}
}

extension GCCell {
    static var nib:UINib {
        return UINib(nibName: identifier, bundle: nil)
    }
    
    static var identifier: String {
        return String(describing: self)
    }
}
